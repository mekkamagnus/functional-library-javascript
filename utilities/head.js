/**
 * Returns the first element in an array
 * @param {Array} xs - The initial array.
 * @returns {Array} - The initial array.
 * @param {Array} - The initial array.
 * @example
 * const colors = ['red', 'blue', 'green', 'yellow'];
 * head(colors)
 * //> 'red'
 */

export const head = xs => xs[0];

